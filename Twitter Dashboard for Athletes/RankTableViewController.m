- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    
    //query
    PFQuery *query = [PFQuery queryWithClassName:@"UserTweetCount"];
    [query orderByDescending:@"count"];
    
    //get the objects
    NSArray *athleteTweetCounts = [query findObjects];
    self.ranks = athleteTweetCounts;
    self.rows = athleteTweetCounts.count;
    
    return athleteTweetCounts.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"rankCell" forIndexPath:indexPath];
    
    // Configure the cell...
    PFObject *athlete = [self.ranks objectAtIndex:indexPath.row];
    int count = athlete[@"count"];
    NSString *name = athlete[@"name"];
    cell.textLabel.text = [NSString stringWithFormat:@"@%@", name];
    
    return cell;
}


